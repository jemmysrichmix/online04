$(".menu-slider-left, .menu-slider-right").click(
  function(){
    if ($(".slider-img").is(':visible')) {
      $(".slider-img").hide();
      $(".slider-imgg").fadeIn();
    } else {
      $(".slider-imgg").hide();
      $(".slider-img").fadeIn();
    }
  }
);

